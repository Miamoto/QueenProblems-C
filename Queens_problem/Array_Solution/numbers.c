#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>



/***********************************************************************************/


/*
 *	This function will check if char is a number or not
 */

int isNumber(char *s) {
  while (*s != '\0') {
    if (!isdigit(*s++))
      return 0;
  }
 
  return 1; 
}



